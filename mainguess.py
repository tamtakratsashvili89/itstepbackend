import random  # рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃўрЃЏрЃърЃЮрЃарЃбрЃў

def guess_game():
    print("рЃЏрЃЮрЃњрЃћрЃАрЃљрЃџрЃЏрЃћрЃЉрЃў рЃЌрЃљрЃЏрЃљрЃерЃерЃў Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃфрЃљрЃюрЃў рЃарЃўрЃфрЃ«рЃЋрЃў!")

    # 1) рЃЋрЃЦрЃЏрЃюрЃўрЃЌ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃА 1-рЃЊрЃљрЃю 100-рЃЏрЃЊрЃћ
    secret_number = random.randint(1, 100)

    while True:
        # 2) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ рЃЊрЃљ рЃЋрЃљрЃџрЃўрЃЊрЃљрЃфрЃўрЃљ
        user_input = input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃарЃўрЃфрЃ«рЃЋрЃў (1-100): ")

        try:
            guess = int(user_input)
        except ValueError:
            print("рЃњрЃЌрЃ«рЃЮрЃЋ, рЃЕрЃљрЃгрЃћрЃарЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃЏрЃЌрЃћрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў!")
            continue  # рЃЌрЃљрЃЋрЃўрЃЊрЃљрЃю рЃАрЃЌрЃ«рЃЮрЃЋрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљрЃА

        # рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃўрЃА рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ
        if guess < 1 or guess > 100:
            print("рЃарЃўрЃфрЃ«рЃЋрЃў рЃБрЃюрЃЊрЃљ рЃўрЃДрЃЮрЃА 1-рЃЊрЃљрЃю 100-рЃЏрЃЊрЃћ!")
            continue


        # 3) рЃерЃћрЃЊрЃљрЃарЃћрЃЉрЃљ рЃЊрЃљ рЃЏрЃўрЃюрЃўрЃерЃюрЃћрЃЉрЃћрЃЉрЃў
        if guess < secret_number:
            print("­Ъћ╝ рЃБрЃцрЃарЃЮ рЃЏрЃљрЃдрЃљрЃџрЃўрЃљ!")
        elif guess > secret_number:
            print("­Ъћй рЃБрЃцрЃарЃЮ рЃЊрЃљрЃЉрЃљрЃџрЃўрЃљ!")
        else:
            # 4) рЃњрЃљрЃЏрЃЮрЃўрЃфрЃюрЃЮ Рєњ рЃерЃћрЃЊрЃћрЃњрЃўрЃА рЃЕрЃЋрЃћрЃюрЃћрЃЉрЃљ рЃЊрЃљ break
            print(f"рЃњрЃўрЃџрЃЮрЃфрЃљрЃЋ! рЃерЃћрЃю рЃњрЃљрЃЏрЃЮрЃўрЃфрЃљрЃюрЃў рЃарЃўрЃфрЃ«рЃЋрЃў {secret_number}!")
            break  # рЃфрЃўрЃЎрЃџрЃўрЃА рЃЊрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃљ

# рЃЌрЃљрЃЏрЃљрЃерЃўрЃА рЃњрЃљрЃерЃЋрЃћрЃЉрЃљ
guess_game()
